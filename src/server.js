/* eslint-disable max-len */
const Hapi = require('@hapi/hapi');
const Jwt = require('@hapi/jwt');
const { sequelize } = require('./models');
const ClientError = require('./exceptions/ClientError');
// dotenv config
require('dotenv').config();
// importing all the instances
// importing the api
const {
  authentications, collaborations, playlists, songs, users,
} = require('./api');
// importing the services
const {
  AuthenticationsService, CollaborationsService, PlaylistsService, SongsService, UsersService,
} = require('./services/postgres');
// importing the validator
const {
  AuthenticationsValidator, CollaborationsValidator, PlaylistValidator, SongsValidator, UsersValidator,
} = require('./validator');
// importing the token
const TokenManager = require('./tokenize/TokenManager');

// initializing the server
const init = async () => {
  const songsService = new SongsService();
  const usersService = new UsersService();
  const authenticationsService = new AuthenticationsService();
  const collaborationsService = new CollaborationsService();
  const playlistsService = new PlaylistsService(collaborationsService);

  const server = Hapi.server({
    port: 5000,
    host: process.env.NODE_ENV !== 'production' ? 'localhost' : '0.0.0.0',
    routes: {
      cors: {
        origin: ['*'],
      },
    },
  });

  // registrasi plugin eksternal
  await server.register([
    {
      plugin: Jwt,
    },
  ]);

  // mendefinisikan strategy autentikasi jwt
  server.auth.strategy('songsapp_jwt', 'jwt', {
    keys: process.env.ACCESS_TOKEN_KEY,
    verify: {
      aud: false,
      iss: false,
      sub: false,
      maxAgeSec: process.env.ACCESS_TOKEN_AGE,
    },
    validate: (artifacts) => ({
      isValid: true,
      credentials: {
        id: artifacts.decoded.payload.id,
      },
    }),
  });

  await server.register([
    {
      plugin: songs,
      options: {
        service: songsService,
        validator: SongsValidator,
      },
    },
    {
      plugin: users,
      options: {
        service: usersService,
        validator: UsersValidator,
      },
    },
    {
      plugin: authentications,
      options: {
        authenticationsService,
        usersService,
        tokenManager: TokenManager,
        validator: AuthenticationsValidator,
      },
    },
    {
      plugin: playlists,
      options: {
        service: playlistsService,
        validator: PlaylistValidator,
      },
    },
    {
      plugin: collaborations,
      options: {
        collaborationsService,
        playlistsService,
        validator: CollaborationsValidator,
      },
    },
  ]);

  // error handling
  server.ext('onPreResponse', (request, h) => {
    // mendapatkan konteks response dari request
    const { response } = request;

    if (response instanceof ClientError) {
      // membuat response baru dari response toolkit sesuai kebutuhan error handling
      const newResponse = h.response({
        status: 'fail',
        message: response.message,
      });
      newResponse.code(response.statusCode);
      return newResponse;
    }

    // jika bukan ClientError, lanjutkan dengan response sebelumnya (tanpa terintervensi)
    return response.continue || response;
  });

  await server.start();

  // eslint-disable-next-line no-console
  console.log(`Server berjalan pada ${server.info.uri}`);

  // initializing the database
  await sequelize.authenticate();
  // eslint-disable-next-line no-console
  console.log('database on');
};

init();
